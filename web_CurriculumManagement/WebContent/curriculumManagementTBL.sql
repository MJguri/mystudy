DROP TABLE COURSE_TBL;

CREATE TABLE COURSE_TBL(
    ID VARCHAR2(5) PRIMARY KEY,
    NAME VARCHAR2(40),
    CREDIT NUMBER(6),
    LECTURER NUMBER(6),
    WEEK NUMBER(2),
    START_HOUR NUMBER(4),
    END_HOUR NUMBER(4)
    );
    
SELECT * FROM COURSE_TBL;

